package Wrapper_Classes_In_Number_Class;

public class Wrapper_Classes_In_Boolean_Class {

	public static void main(String[] args) {
		
	// Java program to demonstrate parseBoolean() method
		
		boolean b1 = Boolean.parseBoolean("True");
		boolean b2 = Boolean.parseBoolean("False");
		boolean b3 = Boolean.parseBoolean("GeeksforGeeks");
		boolean b4 = Boolean.parseBoolean("just");
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		System.out.println(b4);
	}
      
}