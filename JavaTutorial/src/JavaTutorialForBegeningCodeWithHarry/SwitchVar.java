 package JavaTutorialForBegeningCodeWithHarry;

public class SwitchVar {

	public static void main(String[] args) {
		
		String var = "meghraj";
		
		switch(var) {
		
		case "Rushikesh":
			System.out.println("Yes iam Rushikesh");
			break;
			
		case "meghraj":
			System.out.println("Yes iam meghraj");
			break;
			
		case "shiva":
			System.out.println("Yes iam shiva");
			break;
			
		case "jeet":
			System.out.println("yes iam jeet");
			break;
			
		default:
			System.out.println("Enjoy your life");
			
		}
	

	}

}
