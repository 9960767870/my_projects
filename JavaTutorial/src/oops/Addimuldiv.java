package oops;

public class Addimuldiv {
	
	int num1 = 100;
	int num2 = 200;
	int result;
	
	public void add() {
		result = num1+num2;
		System.out.println(result);
	}
	
	public void sub() {
		result = num1-num2;
		System.out.println(result);
	}
	
	public void mul() {
		result = num1*num2;
		System.out.println(result);
	}
	
	public void div() {
		result = num1/num2;
		System.out.println(result);
	}
	

}
