public class ArrayExample2 {
	
	public static void main(String[] args) {
		
		String[] testArray= {"hi","rushi","bhosale"};
		
		System.out.println("The value of String Array is : ");
		
		for(int a=0 ; a<testArray.length ; a++) {
			
			System.out.println(a + ") " + testArray[a]);
			
		}
		
		
	}

}
